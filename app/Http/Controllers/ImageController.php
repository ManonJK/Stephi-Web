     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, $id_bien)
    {
        $request->validate([
            'attachment'=>'required',
        ]);

        $file = $request->file('attachment');
        $path = $file->storeAs('images',time().'.'.$file->extension(),'public');
        $link = time().'.'.$file->extension();
        $img = new Image([
            'id_bien'=>$id_bien,
            'lien'=>'images/'.$link,
        ]);
        $img->save();

        return back();
    }

